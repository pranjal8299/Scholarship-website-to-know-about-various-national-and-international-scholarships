function displayAllScholarships() {
    let linksContainer = document.getElementById('linksContainer');
    if (!linksContainer) {
        linksContainer = document.createElement('div');
        linksContainer.id = 'linksContainer';
        document.body.appendChild(linksContainer);
    } else {
        linksContainer.innerHTML = ''; 
    }

    for (let scholarship of scholarships) {
        if (scholarship.university) {
            let container = document.createElement('div');
            container.classList.add('image-container');

            let image = document.createElement('img');
            image.src = scholarship.photo;

            let textOverlay = document.createElement('div');
            textOverlay.classList.add('text-overlay');
            textOverlay.innerHTML = `
                <p>Description: ${scholarship.description}</p>
                <p>Country: ${scholarship.country}</p>
                <p>State: ${scholarship.state}</p>
                <p>Gender: ${scholarship.gender}</p>
                <p>Category: ${scholarship.category}</p>
                <p>Class: ${scholarship.class}</p>
                <p>University: ${scholarship.university}</p>
                <p><a href="${scholarship.visit_website}" target="_blank">Visit Website</a></p>
            `;

            container.appendChild(image);
            container.appendChild(textOverlay);
            linksContainer.appendChild(container);
        }
    }
}

function matchAndDisplay(data, university) {
    let linksContainer = document.getElementById('linksContainer');
    if (!linksContainer) {
        linksContainer = document.createElement('div');
        linksContainer.id = 'linksContainer';
        document.body.appendChild(linksContainer);
    } else {
        linksContainer.innerHTML = '';
    }

    let matchedElements = [];

    for (let scholarship of scholarships) {
        if (scholarship.university && scholarship.university.toLowerCase() === university.toLowerCase()) {
            matchedElements.push(scholarship);
        }
    }

    for (let scholarship of matchedElements) {
        let container = document.createElement('div');
        container.classList.add('image-container');

        let image = document.createElement('img');
        image.src = scholarship.photo;

        let textOverlay = document.createElement('div');
        textOverlay.classList.add('text-overlay');
        textOverlay.innerHTML = `
            <p>Description: ${scholarship.desc}</p>
            <p>Country: ${scholarship.country}</p>
            <p>State: ${scholarship.state}</p>
            <p>Gender: ${scholarship.gender}</p>
            <p>Category: ${scholarship.category}</p>
            <p>Class: ${scholarship.class}</p>
            <p>University: ${scholarship.university}</p>
            <p><a href="${scholarship.visit_website}" target="_blank">Visit Website</a></p>
        `;

        container.appendChild(image);
        container.appendChild(textOverlay);
        linksContainer.appendChild(container);
    }
}

function refresh() {
    let university = document.getElementById('university').value.trim();

    if (university === "") {
        displayAllScholarships();
    } else {
        let matchedScholarships = scholarships.filter(scholarship => scholarship.university && scholarship.university.toLowerCase() === university.toLowerCase());
        if (matchedScholarships.length > 0) {
            matchAndDisplay(matchedScholarships, university);
        } else {
            let linksContainer = document.getElementById('linksContainer');
            linksContainer.innerHTML = '';

            let noResultMessage = document.createElement('p');
            noResultMessage.textContent = 'There are no scholarships available for the entered university.';
            linksContainer.appendChild(noResultMessage);
        }
    }
}
